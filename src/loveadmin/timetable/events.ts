function fixData(events: TimetableEvent[]): TimetableEvent[] {
  const eventColours = Object.keys(eventColourClasses) as EventColour[];
  const bgColours = Object.keys(bgColourClasses) as BgColour[];
  const descriptionToColourMap = new Map<
    string,
    { eventColour: EventColour; bgColour: BgColour }
  >();
  let colourIndex = 0;

  events.forEach((event) => {
    if (!descriptionToColourMap.has(event.description)) {
      descriptionToColourMap.set(event.description, {
        eventColour: eventColours[colourIndex],
        bgColour: bgColours[colourIndex],
      });
      colourIndex =
        (colourIndex + 1) % Math.max(eventColours.length, bgColours.length);
    }

    const colours = descriptionToColourMap.get(event.description);
    if (colours) {
      event.eventColour = colours.eventColour;
      event.bgColour = colours.bgColour;
    }

    event.fullStartTime = new Date(`${event.date}T${event.startTime}`);
    event.fullEndTime = new Date(`${event.date}T${event.endTime}`);

    // Adding addressName with value 'Wimbledon' only if address is populated
    if (event.address) {
      event.addressName = "Wimbledon";
    }
  });

  return events;
}

export const events = fixData(backflipEvents);
